// A fragments of code that produces a value is called Expression
console.log("Operators in JS");
let a = 45;
let b = 9;
// Addition
console.log("a + b = ", a + b);
// Subtraction
console.log("a - b = ", a - b);
// Multiplication
console.log("a * b = ", a * b);
// Division
console.log("a / b = ", a / b);
// Modulo
console.log("a % b = ", a % b);
// Power
console.log("a ** b = ", a ** b);
// Post Increment
console.log("a++ =", a++);
// Pre Increment
console.log("++a =", ++a);
// Post Decrement
console.log("a-- =", a--);
// Pre Decrement
console.log("--a =", --a);
